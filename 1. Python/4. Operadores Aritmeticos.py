# Operadores Aritmeticos

# Petición de número a ejecutar operación
valor1 =  int(input("Ingresa el primer valor: ").strip())
valor2 =  int(input("Ingresa el segundo valor: ").strip())

# Suma
print(f'El resultado de la suma es: {valor1 + valor2}')

# Resta
print(f'El resultado de la resta es: {valor1 - valor2}')

# Multiplicación
print(f'El resultado de la multiplicación es: {valor1 * valor2}')

# División
print(f'El resultado de la división es: {valor1 / valor2}')

# Modulo
print(f'El resultado de la modulo es: {valor1 % valor2}')